export const getNumberById = (id) => {
  return NUMBERS_DATA.find(num => num.id === id);
};

export const getNumberByValue = (value) => {
  return NUMBERS_DATA.find(num => num.number === String(value));
};

export const getNumbersByCategory = (category) => {
  return NUMBERS_DATA.filter(num => num.category === category);
};

export const getNumbersByDifficulty = (difficulty) => {
  return NUMBERS_DATA.filter(num => num.difficulty === difficulty);
};

export const searchNumbers = (query) => {
  const lowerQuery = query.toLowerCase();
  return NUMBERS_DATA.filter(num =>
    num.number.includes(query) ||
    num.word.toLowerCase().includes(lowerQuery) ||
    num.sentence.toLowerCase().includes(lowerQuery) ||
    num.examples.some(ex => ex.toLowerCase().includes(lowerQuery))
  );
};