/**
 * Get letter by ID
 */
export const getLetterById = (id) => {
  return ALPHABET_DATA.find(letter => letter.id === id);
};

/**
 * Get letters by category
 */
export const getLettersByCategory = (category) => {
  return ALPHABET_DATA.filter(letter => letter.category === category);
};

/**
 * Get letters by difficulty
 */
export const getLettersByDifficulty = (difficulty) => {
  return ALPHABET_DATA.filter(letter => letter.difficulty === difficulty);
};

/**
 * Search letters and vocabulary
 */
export const searchAlphabet = (query) => {
  const lowerQuery = query.toLowerCase();
  
  const letterResults = ALPHABET_DATA.filter(letter => 
    letter.letter.toLowerCase().includes(lowerQuery) ||
    letter.lowercase.includes(lowerQuery) ||
    letter.description.toLowerCase().includes(lowerQuery)
  );
  
  const wordResults = ALPHABET_DATA.filter(letter =>
    letter.words.some(word =>
      word.word.toLowerCase().includes(lowerQuery) ||
      word.sentence.toLowerCase().includes(lowerQuery) ||
      word.description.toLowerCase().includes(lowerQuery)
    )
  );
  
  // Combine and deduplicate
  const combined = [...letterResults, ...wordResults];
  return [...new Set(combined)];
};

/**
 * Get vocabulary words for a letter
 */
export const getVocabularyForLetter = (letterId) => {
  const letter = getLetterById(letterId);
  return letter ? letter.words : [];
};

